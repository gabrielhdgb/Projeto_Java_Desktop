    private void jtVendasPesqMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtVendasPesqMouseClicked
        // TODO add your handling code here:

        try {
            valorUni.setText("");
            listaDePedidos = DAOPedido.preencheComboBox(1);
            DefaultComboBoxModel cbModelPed = (DefaultComboBoxModel) jcPedidos.getModel();
            
            for (Pedido ped : listaDePedidos) {
                cbModelPed.addElement(ped.getIdPedido());
            }

            String nomeProduto = "";

            int linhaSelecionada = jtVendasPesq.getSelectedRow();
            for (Produto produto : listaDeProdutos) {
                if (produto.getIdProduto() == listaVendidos.get(linhaSelecionada).getProduto().getIdProduto()) {
                    nomeProduto = produto.getNomeProd();
                }
            }

            if (linhaSelecionada > -1) {

                qtdVenda.setText("" + listaVendidos.get(linhaSelecionada).getQtdProduto());
                valorTotal.setText("" + listaVendidos.get(linhaSelecionada).getValorTotal());
                jcPedidos.setSelectedItem(listaVendidos.get(linhaSelecionada).getPedido().getIdPedido());
                jcProdutos.setSelectedItem(nomeProduto);

                jcPedidos.setEnabled(false);
                jcProdutos.setEnabled(false);
                btnAdicionar.setEnabled(false);
                btnRemoverPedido.setEnabled(false);
                btnSalvarPedidos.setEnabled(false);
                btnEditar.setEnabled(true);
                btnExcluir.setEnabled(true);
                btnCancelar.setEnabled(true);

            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            // JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jtVendasPesqMouseClicked

    private void pesqVendasKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pesqVendasKeyReleased
        // TODO add your handling code here:

        try {
            DefaultTableModel mp = (DefaultTableModel) jtVendasPesq.getModel();
            mp.setNumRows(0);

            if (pesqVendas.getText().length() > 0) {
                listaVendidos = DAOVendas.pesquisar(pesqVendas.getText());
                for (Vendas v : listaVendidos) {
                    mp.addRow(new String[]{"" + v.getPedido().getIdPedido(), "" + v.getProduto().getIdProduto(),
                        "" + v.getQtdProduto(), "" + v.getValorTotal()});
                }
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_pesqVendasKeyReleased

