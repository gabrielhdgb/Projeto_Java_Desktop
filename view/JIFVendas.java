    private boolean preencherObjeto() {
        try {

            if (acao == 2) {
                float valorTot, valorU;
                int qtd;
                qtd = Integer.parseInt(qtdVenda.getText());
                valorU = Float.parseFloat(valorUni.getText());
                valorTot = qtd * valorU;

                objVendas.setQtdProduto(Integer.parseInt(qtdVenda.getText()));
                objVendas.setValorTotal(valorTot);
                
                Pedido pedidoSelecionado = new Pedido();
                for (Pedido ped : listaDePedidos) {
                    if (ped.getIdPedido() == Integer.parseInt(jcPedidos.getSelectedItem() + "")) {
                        pedidoSelecionado = ped;
                        System.out.println("Entrou!");
                    }
                }
                objVendas.setPedido(pedidoSelecionado);
                objVendas.setProduto((listaDeProdutos.get(jcProdutos.getSelectedIndex())));

            } else {

                for (int i = 0; i < jtVendas.getRowCount(); i++) {
                    Produto prod = new Produto();
                    Pedido ped = new Pedido();
                    ped.setIdPedido(Integer.parseInt(jtVendas.getValueAt(i, 0).toString()));
                    prod.setIdProduto(Integer.parseInt(jtVendas.getValueAt(i, 1).toString()));
                    Vendas venda = new Vendas(Integer.parseInt(jtVendas.getValueAt(i, 2).toString()),
                            Float.parseFloat(jtVendas.getValueAt(i, 3).toString()), prod, ped);
                    listaDeVendas.add(venda);
                }
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        return true;

    }

