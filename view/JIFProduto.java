    private boolean preencherObjeto() {

        if (acao == 2) {
            obj.setIdProduto(Integer.parseInt(idProduto.getText()));
        }
        obj.setNomeProd(nomeProd.getText());
        obj.setValorProd(Float.parseFloat(valorProd.getText()));
        obj.setQtdProd(Integer.parseInt(qtdProd.getText()));
        obj.setFornecedor(listaDeFornecedor.get(jcFornecedor.getSelectedIndex()));

        return true;
    }

