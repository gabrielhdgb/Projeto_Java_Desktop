    private void jtProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtProdutoMouseClicked
        // TODO add your handling code here:
        try {
            int linhaSelecionada = jtProduto.getSelectedRow();

            System.out.println(listaDeProdutos.get(linhaSelecionada).getFornecedor().getNomeFornecedor());

            if (linhaSelecionada > -1) {

                idProduto.setText("" + listaDeProdutos.get(linhaSelecionada).getIdProduto());
                nomeProd.setText(listaDeProdutos.get(linhaSelecionada).getNomeProd());
                qtdProd.setText("" + listaDeProdutos.get(linhaSelecionada).getQtdProd());
                valorProd.setText("" + listaDeProdutos.get(linhaSelecionada).getValorProd());
                jcFornecedor.setSelectedItem(listaDeProdutos.get(linhaSelecionada).getFornecedor().getNomeFornecedor());

                btnCadastrar.setEnabled(false);
                btnEditar.setEnabled(true);
                btnExcluir.setEnabled(true);
                pesquisarProd.setEnabled(true);
                btnNovo.setEnabled(false);
                btnCancelar.setEnabled(true);
                nomeProd.setEnabled(true);
                qtdProd.setEnabled(true);
                valorProd.setEnabled(true);
                jcFornecedor.setEnabled(true);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jtProdutoMouseClicked

    private void pesquisarProdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pesquisarProdKeyReleased
        // TODO add your handling code here:
        try {
            DefaultTableModel mp = (DefaultTableModel) jtProduto.getModel();
            mp.setNumRows(0);

            if (pesquisarProd.getText().length() > 0) {
                listaDeProdutos = DAOProduto.pesquisarProdutosGeral(pesquisarProd.getText());
                for (Produto p : listaDeProdutos) {
                    mp.addRow(new String[]{
                        "" + p.getNomeProd(), "" + p.getQtdProd(), "" + p.getFornecedor()});
                }
            } else {
                listaDeProdutos = DAOProduto.pesquisarProdutosGeral("''");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_pesquisarProdKeyReleased

